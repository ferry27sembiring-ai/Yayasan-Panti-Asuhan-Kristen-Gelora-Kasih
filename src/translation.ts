/**
 * Clean & reliable translator helper
 */
export function translateText(text: string, lang: "id" | "en"): string {
  if (lang === "id" || !text) return text;
  
  const trimmed = text.trim();
  if (EN_DICT[trimmed]) {
    return EN_DICT[trimmed];
  }
  
  // Check substrings or case variations
  for (const [key, value] of Object.entries(EN_DICT)) {
    if (trimmed.toLowerCase() === key.toLowerCase()) {
      return value;
    }
  }

  // Handle some common categories translations dynamically
  if (trimmed === "Ibadah") return "Worship";
  if (trimmed === "Kunjungan") return "Visitor Visit";
  if (trimmed === "Pendidikan") return "Education";
  if (trimmed === "Bakti Sosial") return "Social Action";
  if (trimmed === "Lain-lain") return "Others";

  return text;
}
